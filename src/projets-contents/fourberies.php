		Projet d'édition autour d'une célèbre pièce de théâtre : les fourberies de Scapin. 
		
		J'ai pensé l'objet en tant que décor de théâtre en jouant sur la transparence du papier. Une fois le décor éclairé en place, un texte apparaît et donne le ton pour la scène. Une page = une scène. Un volet du livre = un acte. Une couleur = un personnage différent. On voit alors le décor, l'ambiance colorée de la pièce changer en même temps que celle-ci est jouée.
