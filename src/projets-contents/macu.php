</header>





<section class="texte">


	<p class="description">

Réalisation d'un programme de communication pour la Maison de la Culture d'Amiens. 

Pour ce projet, j'ai conçu douze typographies modulaires pour les douze spectacles présentés. Chacune de ces typographies est basée sur une forme géométrique simple, inspirée par un spectacle. L'utilisation d'une typographie par spectacle créée la cohérence visuelle. Une couleur est aussi associée à chacun des spectacles. 

Il y a alors 12 affiches où les formes et typos sont découpés laissant un jeux d'ombres et de lumières ainsi que le support sur lesquelles elles sont affichées apparent. 12 dépliants de présentation des spectacles, 12 programmes présentant les 12 spectacles ainsi qu'un livret présentant les 12 typographies en noir et blanc. 

